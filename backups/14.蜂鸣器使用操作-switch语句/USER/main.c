//主函数
void	main(void) 
{
	Timer0Init();
	Timer1Init();
	DotLed_Init();
	Beep_Init();
	
	while(1)			//死循环
	{
		//每隔1ms要干的事，在这里
		if(Flag_1ms) //1ms时间到
		{
			Flag_1ms = 0;//复位1ms时间标志，等待下一个1ms时间到
			
			DotLed_Display();
		
		}
		
		//每隔10ms要干的事，在这里
		if(Flag_10ms) //10ms时间到
		{
			Flag_10ms = 0;//复位10ms时间标志，等待下一个10ms时间到
			
			SysLed_Display();	//调用系统灯显示函数
		  DotLed_Renew(); 
			LD_LED();
		  Buzzer_Handle();
		}
		
		
		
	}
	 
}